# set is a collection which unordered and unindexed. No duplicate members
fruit_sets = {'apples', 'oranges', 'mango'}

# check if its in the set
print('apples' in fruit_sets)
# add to set
fruit_sets.add('grape')
# remove a set
fruit_sets.remove('grape')
# clear a set
fruit_sets.clear()
# delete a set
# del fruit_sets
print(fruit_sets)
